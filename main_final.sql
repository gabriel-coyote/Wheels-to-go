DROP DATABASE IF EXISTS CompanyDB;
CREATE DATABASE CompanyDB;


-- Creating CompanyDB Schema
USE CompanyDB;

DROP TABLE IF EXISTS VEHICLE;
CREATE TABLE VEHICLE (
  Platenumber varchar(7),
  Year int(4),
  Price int(6),
  Make varchar(32),
  Model varchar(32),
  StoreRegistrationID varchar(8),
  CONSTRAINT pk_Pnum PRIMARY KEY (Platenumber)
);

-- ##########################################
DROP TABLE IF EXISTS CUSTOMER;
CREATE TABLE CUSTOMER (
  Firstname varchar(32),
  Lastname varchar(32),
  Store varchar(32),
  DLnumber varchar(8),
  CreditcardNumber varchar(16),
  Platenumber varchar(7),
  Ssn bigint(10),
  CONSTRAINT pk_DLnum PRIMARY KEY (DLnumber),
  CONSTRAINT fk_customer_vehicle FOREIGN KEY (Platenumber) REFERENCES VEHICLE(Platenumber)
);

-- ##########################################
DROP TABLE IF EXISTS STORE;
CREATE TABLE STORE (
  Location varchar(32),
  StoreRegistrationID int(8),
  CONSTRAINT pk_Store PRIMARY KEY (StoreRegistrationID)
);

-- ##########################################
DROP TABLE IF EXISTS EMPLOYEE;
CREATE TABLE EMPLOYEE (
  Salary int(6),
  Sex varchar(8),
  Address varchar(128),
  Birthdate date,
  Lastname varchar(32),
  Firstname varchar(32),
  Ssn bigint(10),
  WorksAt int(8), /* not in documentation */
  Id int(5),
  CONSTRAINT pk_employee PRIMARY KEY (Id),
  CONSTRAINT fk_employee_store FOREIGN KEY (WorksAt) REFERENCES STORE(StoreRegistrationID)
);

-- ##########################################
DROP TABLE IF EXISTS CLERK;
CREATE TABLE CLERK (
  Id int(5),
  Services varchar(8),
  CONSTRAINT fk_clerk_employee FOREIGN KEY (Id) REFERENCES EMPLOYEE(Id),
  CONSTRAINT fk_clerk_customer FOREIGN KEY (Services) REFERENCES CUSTOMER(DLnumber)
);



-- ##########################################
DROP TABLE IF EXISTS MANAGER;
CREATE TABLE MANAGER (
  Id int(5),
  Manages int(8),
  CONSTRAINT fk_manager_employee FOREIGN KEY (Id) REFERENCES EMPLOYEE(Id),
  CONSTRAINT fk_manager_store FOREIGN KEY (Manages) REFERENCES Store(StoreRegistrationID)
);

-- ##########################################
DROP TABLE IF EXISTS MECHANIC;
CREATE TABLE MECHANIC (
  Id int(5),
  Fixes varchar(7),
  CONSTRAINT fk_mechanic_employee FOREIGN KEY (Id) REFERENCES EMPLOYEE(Id),
  CONSTRAINT fk_mechanic_vehicle FOREIGN KEY (Fixes) REFERENCES VEHICLE(Platenumber)
);



